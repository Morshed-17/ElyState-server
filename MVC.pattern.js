// MVC = model view controller

// model = It manages the data, business rules, and logic of the application. The model is responsible for retrieving and storing data, as well as notifying observers (such as the view) about changes in the data.

// view = This represents the user interface or presentation layer of the application. It is responsible for displaying the data to the user and sending user input to the controller.


// controller = the brain or traffic cop of a software application. Its main job is to receive input from the user (via the view), process that input, interact with the model (which manages data and logic), and then update the view accordingly. 